package Basics;
public class RelationalOperators {

    public static void main(String args[])
    {
        int a=5;
        int b=7;
        System.out.println("a is "+a+" and b is "+b);
        System.out.println(a==b);//false
        System.out.println(a!=b);//true
        System.out.println(a>b);//false
        System.out.println(a<b);//true
        System.out.println(a>=b);//false 
        System.out.println(a<=b);////true

    }
    
}


