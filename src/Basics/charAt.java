
package Basics;

/*
SYNTAX
string.charAt(int index);

*/

public class charAt {
    
    public static void main(String args[])
    {
        String s1="Hello Java";
        System.out.println(s1.charAt(0));//H
        System.out.println(s1.charAt(6));//J
        System.out.println(s1.charAt(9));//a


    }
}

/*

OUTPUT

H
J
a

*/