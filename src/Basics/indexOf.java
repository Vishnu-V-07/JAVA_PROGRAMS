
package Basics;

/*
SYNTAX
string.indexOf(char ch);

*/

public class indexOf {
    
    public static void main(String args[])
    {
        String s1="Hello Java";
        System.out.println(s1.indexOf('H'));//0
        System.out.println(s1.indexOf('J'));//6
        System.out.println(s1.indexOf('a'));//7


    }
}

/*

OUTPUT

0
6
7

*/