
package Basics;


/*
SYNTAX

Stringname.hashCode();

*/ 

public class hashCode {
    
    public static void main(String args[])
    {
        String s1="Java";
        String s2="Programming";
        
        System.out.println(s1.hashCode());
        System.out.println(s2.hashCode());
    }
}

