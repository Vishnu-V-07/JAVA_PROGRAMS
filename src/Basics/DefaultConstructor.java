package Basics;
class DefaultConstructor
{
    int a=10;
    int b=20;

    public static void main(String args[])
    {
        DefaultConstructor obj=new DefaultConstructor();
        System.out.println("a value is "+obj.a);
        System.out.println("b value is "+obj.b);


    }
}


/*
a value is 10
b value is 20
*/